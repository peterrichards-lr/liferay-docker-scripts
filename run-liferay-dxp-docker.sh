#!/bin/zsh

Color_Off='\033[0m'
Green='\033[0;32m'
Yellow='\033[0;33m'
White='\033[0;37m'
BYELLOW='\033[1;33m'
Red='\033[0;31m'
BRed='\033[1;31m'
