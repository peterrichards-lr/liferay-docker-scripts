read_password() { if [[ "$NON_INTERACTIVE" == 1 ]]; then typeset -g "$2"="$3"; return; fi; if [[ -n $* ]]; then local ANSWER; echo -n -e "${White}âť“ $1: ${Color_Off}"; read -rs ANSWER; echo -e ""; typeset -g "$2"="${ANSWER}"; fi }

if ! docker info >/dev/null 2>&1; then
  _die "Docker is not running or not accessible. Please start Docker and try again."
fi

IMAGE_NAME=liferay/dxp
PROJECT_META_FILE=".liferay-docker.meta"

NON_INTERACTIVE=0
QUIET=0
VERBOSE=0
RUN_SELECT=0
ROOT_ARG=""
TAG_ARG=""
CONTAINER_ARG=""
RELEASE_TYPE_ARG=""
DB_KIND=""
JDBC_USERNAME_ARG=""
JDBC_PASSWORD_ARG=""
RECREATE_DB_FLAG=""
HOST_NETWORK_FLAG=""
NO_HOST_NETWORK_FLAG=""
HOST_NAME_ARG=""
