Color_Off='\033[0m'
Green='\033[0;32m'
Yellow='\033[0;33m'
White='\033[0;37m'
BYELLOW='\033[1;33m'
Red='\033[0;31m'
BRed='\033[1;31m'
Cyan='\033[0;36m'

info() { [[ -n $* ]] && echo -e "${Yellow}ℹ $1${Color_Off}"; }
info_custom() { [[ -n $* ]] && echo -e "$1${Color_Off}"; }
error() { echo -e "${BRed}❌ Error:${Color_Off} $*" 1>&2; }
_die() {
	error "$*"
	exit 1
}
read_config() {
	if [[ "$NON_INTERACTIVE" == 1 ]]; then
		typeset -g "$2"="$3"
		return
	fi
	if [[ -n $* ]]; then
		local ANSWER
		echo -n -e "${White}❓ $1 [${Green}$3${White}]: ${Color_Off}"
		read -r ANSWER
		typeset -g "$2"="${ANSWER:-$3}"
	fi
}
read_input() {
	if [[ "$NON_INTERACTIVE" == 1 ]]; then
		typeset -g "$2"="$3"
		return
	fi
	if [[ -n $* ]]; then
		local ANSWER
		echo -n -e "${White}❓ $1: ${Color_Off}"
		read -r ANSWER
		typeset -g "$2"="${ANSWER}"
	fi
}
read_password() {
	if [[ "$NON_INTERACTIVE" == 1 ]]; then
		typeset -g "$2"="$3"
		return
	fi
	if [[ -n $* ]]; then
		local ANSWER
		echo -n -e "${White}❓ $1: ${Color_Off}"
		read -r rs ANSWER
		echo -e ""
		typeset -g "$2"="${ANSWER}"
	fi
}

